      private void cargar_tabla(String id) {
        
        try {
            
            String sql="";
            
         
            
            sql = "SELECT s.marca, s.modelo, s.color, s.nro_chapa\n" +
                    "FROM servicios_estacionamiento s\n" +
                    "left join servicios ss on s.servicio_id = ss.id\n" +
                    "WHERE ss.id = "+id+" ";
            
           
            
            t.setRowCount(0);
            con.sentencia = con.conexion.createStatement();
            con.resultado = con.sentencia.executeQuery(sql);
            while (con.resultado.next()) {
                t.addRow(new Object[]{con.resultado.getString("s.marca"), 
                    con.resultado.getString("s.modelo"),
                    con.resultado.getString("s.color"),
                    //num.format(con.resultado.getInt("p.nro_documento")),
                   con.resultado.getString("s.nro_chapa")});
                
            }  
        
        
        } catch (SQLException ex) {
            Logger.getLogger(buscador_servicios.class.getName()).log(Level.SEVERE, null, ex);
        }
}
    

