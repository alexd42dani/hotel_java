    private void btn_aceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_aceptarActionPerformed
       // referenciales.huespedes.buscador_personas("1");
       
       
       
       if(tabla_equipaje.getRowCount()<=0){
           JOptionPane.showMessageDialog(null, "No ha cargado ningun detalle"); 
       
       
       }else{
           
           f = tabla_equipaje.getRowCount();
           c = 4;
           tabla = new String[f][c];
           
           for (int c1=0; c1<c;c1++){
               for(int f1=0; f1<f;f1++){
                   if(c1==0){
                      tabla[f1][c1] = tabla_equipaje.getValueAt(f1,0).toString();
                      //precio += Integer.parseInt(tabla_equipaje.getValueAt(f1,2).toString());
                   }else if(c1==1){
                      tabla[f1][c1] = tabla_equipaje.getValueAt(f1,1).toString();
                   }else if(c1==2){
                      tabla[f1][c1] = tabla_equipaje.getValueAt(f1,2).toString();
                   }else if(c1==3){
                      tabla[f1][c1] = tabla_equipaje.getValueAt(f1,3).toString();
                   }
               }
           }
           
       }
       
       if(formulario.equals("servicios")){
           movimientos.servicios.servicios.detalle_servicios(tabla, f, c, precio);
       }
       
       
       f = -1;
       c = -1;
       tabla = null;
       precio=0;
       
      // dispose();
        moveToBack();
    }//GEN-LAST:event_btn_aceptarActionPerformed

