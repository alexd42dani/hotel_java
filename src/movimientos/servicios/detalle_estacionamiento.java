    private void btn_editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editarActionPerformed
         if (tabla_equipaje.getSelectedRowCount() == 0) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar por lo menos una fila", "Atencion",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }
            
        if (txt_color.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe cargar color");
            txt_color.requestFocus();
        }if (txt_chapa.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe cargar chapa");
            txt_chapa.requestFocus();
        }else if (txt_marca.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe cargar marca");
            txt_marca.requestFocus();
        }else if (txt_modelo.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe cargar modelo");
            txt_modelo.requestFocus();
        } else {
      
        int mensaje = JOptionPane.showConfirmDialog(null, "Desea editar la fila seleccionada", "Atencion",
        JOptionPane.WARNING_MESSAGE, JOptionPane.QUESTION_MESSAGE);
        if (mensaje == 0) {
            javax.swing.table.DefaultTableModel tab = (javax.swing.table.DefaultTableModel) tabla_equipaje.getModel();
            tab.removeRow(tabla_equipaje.getSelectedRow());
            cargar_detalle();
            txt_marca.requestFocus();
        }else {
        btn_limpiar.doClick();
        }
        }
    }//GEN-LAST:event_btn_editarActionPerformed

